package modeleDirection;

public class Direction {
	private int directionID;
	private String libelleDirection;
	private String abreviationDirection;
	public int getDirectionID() {
		return directionID;
	}
	public void setDirectionID(int directionID) {
		this.directionID = directionID;
	}
	public String getLibelleDirection() {
		return libelleDirection;
	}
	public void setLibelleDirection(String libelleDirection) {
		this.libelleDirection = libelleDirection;
	}
	public String getAbreviationDirection() {
		return abreviationDirection;
	}
	public void setAbreviationDirection(String abreviationDirection) {
		this.abreviationDirection = abreviationDirection;
	}
	
}
